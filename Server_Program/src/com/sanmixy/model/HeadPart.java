package com.sanmixy.model;

public class HeadPart {

	private int id;
	
	private Part first;
	
	private double con;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public Part getFirst() {
		return first;
	}

	public void setFirst(Part first) {
		this.first = first;
	}

	public double getCon() {
		return con;
	}

	public void setCon(double con) {
		this.con = con;
	}
	
}
